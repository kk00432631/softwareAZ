package com.hcl.utilityFactory.model;

public class DeprecatedServices {

	private String serviceName;
	private String serviceReplacement;
	private String workAround;
	public String getServiceName() {
		return serviceName;
	}
	public void setServiceName(String serviceName) {
		this.serviceName = serviceName;
	}
	public String getServiceReplacement() {
		return serviceReplacement;
	}
	public void setServiceReplacement(String serviceReplacement) {
		this.serviceReplacement = serviceReplacement;
	}
	public String getWorkAround() {
		return workAround;
	}
	public void setWorkAround(String workAround) {
		this.workAround = workAround;
	}
	
	
	
}
